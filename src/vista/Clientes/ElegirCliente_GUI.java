    private void butAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butAceptarActionPerformed

        int filaSeleccionada = tabClientesFiltrados.getSelectedRow();

        if (filaSeleccionada != -1) {
            try {
                String dniCliente = String.valueOf(tabClientesFiltrados.getValueAt(filaSeleccionada, 0));
                
                Cliente cTMP = new Cliente();
                cTMP.setDni(dniCliente);
                
                Cliente c = LogicaCliente.getCliente(cTMP);
                imc.panelDeLlamada(c);
                this.setVisible(false);
                this.dispose();
            } catch (Exception ex) {
                System.out.println(ex.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(this, 
                    "ERROR.No hay ningún cliente seleccionado!!", 
                    getName(), 
                    JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_butAceptarActionPerformed

    
