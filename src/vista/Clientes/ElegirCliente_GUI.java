    private void limpiarTabla() {
        int cantidadFilas = tabModel.getRowCount();

        if (cantidadFilas != 0) {
            for (int i = cantidadFilas - 1; i >= 0; i--) {
                tabModel.removeRow(i);
            }
        }
    }

    private void butBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butBuscarActionPerformed

        try {
            limpiarTabla();
            tabClientesFiltrados.setModel(tabModel);
            List<Cliente> tClientes = new ArrayList<>();
            LogicaCliente.listadoClienteFiltrado(tClientes, txtFiltroDni.getText().equals("") ? "%" : txtFiltroDni.getText());

            for (Cliente c : tClientes) {
                tabModel.addRow(new Object[]{
                    c.getDni(),
                    c.getNombre(),
                    c.getDireccion(),
                    c.getTelefono(),
                    c.getEmail()});
            }
        } catch (Exception ex) {
            System.out.println(ex.getMessage());
        }
    }//GEN-LAST:event_butBuscarActionPerformed

