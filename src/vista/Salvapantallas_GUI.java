    public void mostrar(ClinicaDental_GUI imc){
        setVisible(true);
        this.imc = imc;
        
        refrescarPantalla();
    }

    @Override
    public void paint(Graphics g) {
        super.paint(g);
        Graphics2D g2d = (Graphics2D)g;
        try {
            g2d.drawImage(ImageIO.read(new File("src/img/LogoDental.png")), xLogo, yLogo, this);
            moverLogo();
        } catch (IOException ex) {
            System.out.println(ex.getMessage());
        }
    }
    
    private void moverLogo(){
        xLogo++;
        yLogo++;
    }
    
    private void refrescarPantalla(){
        while(true){
            this.repaint();
            try {
                Thread.sleep(10);
            } catch (InterruptedException ex) {
            }
        }
    }

    

